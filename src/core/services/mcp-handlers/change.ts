/**
 * Build the compact risk_context block written into story files.
 */
function buildRiskContextBlock(
  orient: OrientResult,
  impacts: ImpactResult[],
  generatedAt: string,
): string {
  const validImpacts = impacts.filter(i => i.riskScore !== undefined);
  const maxRisk = validImpacts.length > 0
    ? Math.max(...validImpacts.map(i => i.riskScore ?? 0))
    : null;

  const badge = maxRisk === null ? '' : ` ${riskBadge(maxRisk)}`;

  const lines: string[] = [];
  lines.push(`> Auto-generated by spec-gen on ${generatedAt}. Re-run \`annotate_story\` after structural changes.`);
  lines.push('');

  const domains = (orient.specDomains ?? []).map(d => d.domain);
  lines.push(`- **Domains**: ${domains.length > 0 ? domains.join(', ') : 'none detected'}`);

  if (maxRisk !== null) {
    lines.push(`- **Max risk score**: ${maxRisk}${badge}`);
  } else {
    lines.push('- **Max risk score**: unknown (run analyze_codebase first)');
  }

  const topFunctions = (orient.relevantFunctions ?? []).slice(0, 4);
  if (topFunctions.length > 0) {
    const fnList = topFunctions.map(f => `\`${f.name}\` (${f.filePath})`).join(', ');
    lines.push(`- **Functions in scope**: ${fnList}`);
  }

  // Callers to protect = upstream of highest-risk function
  const topImpact = validImpacts[0];
  if (topImpact?.upstreamChain && Array.isArray(topImpact.upstreamChain) && topImpact.upstreamChain.length > 0) {
    const callers = (topImpact.upstreamChain as Array<{ name?: string; filePath?: string }>)
      .slice(0, 3)
      .map(c => `\`${c.name}\``)
      .join(', ');
    lines.push(`- **Callers to protect**: ${callers}`);
  }

  // Blocking refactors
  if (maxRisk !== null && maxRisk >= 70 && validImpacts.length > 0) {
    const blocked = validImpacts
      .filter(i => (i.riskScore ?? 0) >= 70)
      .map(i => `Refactor \`${i.symbol}\` first (risk: ${i.riskScore})`)
      .join('; ');
    lines.push(`- **Blocking refactors**: ${blocked}`);
  } else {
    lines.push('- **Blocking refactors**: none');
  }

  // Insertion points
  const insertionPoints = orient.insertionPoints ?? [];
  if (insertionPoints.length > 0) {
    const top = insertionPoints[0];
    lines.push(`- **Insertion points**: \`${top.name}\` — ${top.strategy} (score: ${top.score})`);
  }

  // Spec domains with file links
  if (domains.length > 0) {
    const specLinks = (orient.specDomains ?? [])
      .slice(0, 3)
      .map(d => `\`${d.specFile}\``)
      .join(', ');
    lines.push(`- **Spec domains linked**: ${specLinks}`);
  }

  // Warning
  if (maxRisk !== null && maxRisk >= 70) {
    lines.push('');
    lines.push('> ⚠️ **Blocked.** Schedule `bmad/tasks/refactor.md` for the high-risk function(s) before this story enters a sprint.');
  } else if (maxRisk !== null && maxRisk >= 40) {
    lines.push('');
    lines.push('> ⚠️ **Caution.** Ensure callers listed above are covered by tests.');
  }

  return lines.join('\n');
}

/**
 * Patch the `## Risk Context` section of a markdown story file in-place.
 *
 * - If the section exists: replaces its body (keeps the heading).
 * - If it doesn't exist: inserts it before `## Technical Constraints`,
 *   `## Tasks`, or `## Dependencies` — whichever comes first.
 *   If none found, appends at end of file.
 */
function patchRiskContext(original: string, newBody: string): string {
  const HEADING = '## Risk Context';
  const lines = original.split('\n');

  const sectionStart = lines.findIndex(l => l.trim() === HEADING);

  if (sectionStart !== -1) {
    // Find end of section = next ## heading (exclusive) or EOF
    let sectionEnd = lines.length;
    for (let i = sectionStart + 1; i < lines.length; i++) {
      if (lines[i].startsWith('## ')) { sectionEnd = i; break; }
    }
    const replacement = [HEADING, '', newBody, ''];
    lines.splice(sectionStart, sectionEnd - sectionStart, ...replacement);
    return lines.join('\n');
  }

  // Section not found — find insertion point
  const insertBefore = ['## Technical Constraints', '## Tasks', '## Dependencies', '## Dev Agent Record'];
  let insertAt = lines.length;
  for (const marker of insertBefore) {
    const idx = lines.findIndex(l => l.trim() === marker);
    if (idx !== -1 && idx < insertAt) insertAt = idx;
  }

  const insertion = ['', HEADING, '', newBody, ''];
  lines.splice(insertAt, 0, ...insertion);
  return lines.join('\n');
}

export async function handleAnnotateStory(
  directory: string,
  storyFilePath: string,
  description: string,
): Promise<unknown> {
  const absDir = await validateDirectory(directory);

  // Resolve story path and ensure it stays within the project directory
  const absStory = safeJoin(absDir, storyFilePath);

  let original: string;
  try {
    original = await readFile(absStory, 'utf8');
  } catch {
    return { error: `Story file not found: ${absStory}` };
  }

  const generatedAt = new Date().toISOString().split('T')[0];

  // ── Structural analysis (same pattern as generate_change_proposal) ─────────
  // spec search not needed for risk_context — orient already returns linked specs
  const orient = await handleOrient(directory, description, 7) as OrientResult;

  const topFunctions = (orient.relevantFunctions ?? []).slice(0, 2);
  const impacts: ImpactResult[] = [];
  for (const fn of topFunctions) {
    try {
      const raw = await handleAnalyzeImpact(directory, fn.name, 2);
      const impact = raw as ImpactResult;
      if (!impact.error) impacts.push(impact);
    } catch { /* non-fatal */ }
  }

  // ── Build and patch ────────────────────────────────────────────────────────
  const riskBlock = buildRiskContextBlock(orient, impacts, generatedAt);
  const patched = patchRiskContext(original, riskBlock);

  await writeFile(absStory, patched, 'utf8');

  // ── Summary ────────────────────────────────────────────────────────────────
  const maxRisk = impacts.length > 0
    ? Math.max(...impacts.map(i => i.riskScore ?? 0))
    : null;
  const domains = (orient.specDomains ?? []).map(d => d.domain);

  return {
    storyFile: storyFilePath,
    domainsAffected: domains,
    functionsFound: (orient.relevantFunctions ?? []).length,
    maxRiskScore: maxRisk,
    riskLevel: maxRisk === null ? 'unknown'
      : maxRisk < 40 ? 'low'
      : maxRisk < 70 ? 'medium'
      : maxRisk < 85 ? 'high'
      : 'critical',
    blocked: maxRisk !== null && maxRisk >= 70,
    orientErrors: orient.error ? [orient.error] : [],
  };
}
